    </p>

    <p style="text-align: justify;">
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ngoài ra, chùa Trấn Quốc còn có nét riêng ấn tượng nhờ vườn tháp chùa cổ xuất hiện từ thế kỷ 18. Trong đó nổi bật hơn cả là tòa Bảo Tháp lục độ đài sen với 11 tầng có độ cao là 15m. Đỉnh tháp có Cửu phẩm liên hoa hay còn gọi là đài sen chín tầng được dựng bằng đá quý.
    </p>

    <p style="text-align: justify;">
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ngôi chùa cổ này đã được Thái hậu Ỷ Lan nhiều lần ghé thăm dưới thời vua Lý Nhân Tông. Đến năm 2017, chùa Trấn Quốc được vinh danh trong 16 ngôi chùa đẹp nhất thế giới do tờ báo Daily Mail bình chọn.
    </p>

    <h3 style="text-align: justify;">
